import UIKit

class Colors: NSObject {
    class func grayBlue() -> UIColor {
        return UIColor.init(red: 61 / 255.0, green: 64 / 255.0, blue: 91 / 255.0, alpha: 1.0)
    }
    class func warmOrange() -> UIColor {
        return UIColor.init(red: 224 / 255.0, green: 122 / 255.0, blue: 95 / 255.0, alpha: 1.0)
    }
    class func pastelWhite() -> UIColor {
        return UIColor.init(red: 244 / 255.0, green: 241 / 255.0, blue: 222 / 255.0, alpha: 1.0)
    }
    class func mossyGreen() -> UIColor {
        return UIColor.init(red: 129 / 255.0, green: 178 / 255.0, blue: 154 / 255.0, alpha: 1.0)
    }
    class func paleYellow() -> UIColor {
        return UIColor.init(red: 242.0 / 255.0, green: 204 / 255.0, blue: 143 / 255.0, alpha: 1.0)
    }
}
